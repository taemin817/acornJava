package day07.func;

public class Ex05 {

	public static void main(String[] args) {
		// 요구사항 : 성적표의 = -> |
		printTitle();
		System.out.println("코드 실행 1");
		printTitle();
		System.out.println("코드 실행 2");
		printTitle();
		
		// 프로그램 종료
	}
	public static void printTitle() {
		System.out.println("|성적표|");
		System.out.println("||||||");
		System.out.println("||||||");
	}
}
